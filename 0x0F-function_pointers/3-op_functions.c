#include "3-calc.h"

/**
 * op_add - the sum of two integer
 * @a: first one
 * @b: second one
 * Return: a+b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - the diffenrence of two integer
 * @a: the first one
 * @b: the second one
 * Return: a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - the product of two integer
 * @a: the first one
 * @b: the second one
 * Return: a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the division of two integer
 * @a: the first one
 * @b: the second one
 * Return: a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the remainder of two integer
 * @a: the first one
 * @b: the second one
 * Return: amodb
 */
int op_mod(int a, int b)
{
	return (a % b);
}
